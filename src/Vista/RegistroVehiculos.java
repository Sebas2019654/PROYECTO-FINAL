    //METODO LISTAR
    void listar() {
        String sql = "SELECT * FROM vehiculos";
        try {
            cn = con.Conectarse();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] veh = new Object[6];
            modelo = (DefaultTableModel) jVehiculos.getModel();
            while (rs.next()) {
                veh[0] = rs.getString("numplaca");
                veh[1] = rs.getString("tipo");
                veh[2] = rs.getString("marca");
                veh[3] = rs.getString("modelo");
                veh[4] = rs.getDate("matricula");
                veh[5] = rs.getString("color");
                modelo.addRow(veh);

            }
            jVehiculos.setModel(modelo);
        } catch (Exception e) {
        }
    }

