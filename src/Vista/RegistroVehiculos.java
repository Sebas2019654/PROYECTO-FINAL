    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Desea eliminar  este vehiculo", "Si/No", 0);
        if (s == 0) {
            int res = 0;
            int row = jVehiculos.getSelectedRow();
            int fila = jVehiculos.getSelectedColumnCount();
            if (fila < 1) {
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR UN REGISTRO DE LA TABLA", "AVISO", JOptionPane.INFORMATION_MESSAGE);

            } else {
                res = sql.eliminarRegistro(jVehiculos.getValueAt(row, 0).toString());
                if (res >= 0) {
                    limpiartabla();
                    listar();
                    Clear();
                }
            }
            txtPlaca.setEditable(true);
            txtPlaca.setEnabled(true);

        }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Desea registrar este vehiculo", "Si/No", 0);
        if (s == 0) {
            txtPlaca.setEditable(true);
            txtPlaca.setEnabled(true);
            boolean estado = false;
            if (txtPlaca.getText().isEmpty() || txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty() || txtColor.getText().isEmpty()) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "*CAMPOS OBLIGATORIOS");
//             CARRO, MOTO, CAMION
            } else {
                if (ValidaPlaca(txtPlaca.getText()) == true) {
                    estado = true;
                    java.sql.Date fecha = null;
                    Date dia = Date.getDate();
                    if (dia != null) {
                        long d = dia.getTime();
                        fecha = new java.sql.Date(d);

                    } else {
                        Date dates = new Date();
                        DateFormat formato = new SimpleDateFormat("dd-MM-yyyy");
                        formato.format(dates);
                        Date.setDate(dates);
                        long d = dia.getTime();
                        fecha = new java.sql.Date(d);
                    }
//              String placa, String tipo, String marca, String modelo, Date matricula, String color

                    Vehiculos vehi = new Vehiculos(txtPlaca.getText(), (String) combocarro.getSelectedItem(), txtMarca.getText(), txtModelo.getText(), fecha, txtColor.getText(), estado);
                    int i = sql.guardar(vehi);

                    if (i > 0) {
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(this, "Vehiculo registrado con exito");
                        limpiartabla();
                        listar();
                        Clear();
                    } else {
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(this, "NO REGISTRADO, YA EXISTE LA PLACA");
                        txtPlaca.setText("");

                    }
                } else {
                    JOptionPane.showMessageDialog(this, "PLACA INVALIDA", "ERROR", JOptionPane.ERROR_MESSAGE);
                    txtPlaca.setText("");
                }

            }
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

