    private void jVehiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jVehiculosMouseClicked
        txtPlaca.setEditable(false);
        txtPlaca.setEnabled(false);
        int fila = jVehiculos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Vehiuclo no seleccionada");
        } else {

            txtPlaca.setText(jVehiculos.getValueAt(fila, 0).toString());
            combocarro.setSelectedItem(jVehiculos.getValueAt(fila, 1).toString());
            txtMarca.setText(jVehiculos.getValueAt(fila, 2).toString());
            txtModelo.setText(jVehiculos.getValueAt(fila, 3).toString());

            txtColor.setText(jVehiculos.getValueAt(fila, 5).toString());
//            java.sql.Date fecha = (java.sql.Date) jVehiculos.getValueAt(fila, 4);
            String fechas = jVehiculos.getValueAt(fila, 4).toString();
            Date dat;
            try {
                dat = new SimpleDateFormat("yyyy-MM-dd").parse(fechas);
                Date.setDate(dat);
            } catch (ParseException e) {
                System.out.println("Eror en fecha de ingreso");
            }

        }

    }//GEN-LAST:event_jVehiculosMouseClicked

