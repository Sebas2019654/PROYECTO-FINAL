    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Esta seguro de modificar este registro", "Si/No", 0);
        if (s == 0) {
            if (txtnombre.getText().isEmpty() || txtapellido.getText().isEmpty() || txtdoc.getText().isEmpty()) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "*CAMPOS OBLIGATORIOS");
            } else {
                boolean estado = false;
                if (ng == 0) {
                    gener = "Masculino";
                    estado = true;
                } else {
                    if (ng == 1) {
                        gener = "Femenino";
                        estado = true;
                    }
                }
                java.sql.Date fecha = null;
                Date dia = date.getDate();
                if (dia != null) {
                    long d = dia.getTime();
                    fecha = new java.sql.Date(d);

                } else {
                    Date dates = new Date();
                    DateFormat formato = new SimpleDateFormat("dd-MM-yyyy");
                    formato.format(dates);
                    date.setDate(dates);
                    long d = dia.getTime();
                    fecha = new java.sql.Date(d);
                }
                Persona people = new Persona((String) CBDocumento.getSelectedItem(), txtdoc.getText(), txtnombre.getText(), txtapellido.getText(), fecha, gener, estado);
                File ruta = new File(txtruta.getText());
                try {
                    byte[] icono = new byte[(int) ruta.length()];
                    InputStream input = new FileInputStream(ruta);
                    input.read(icono);
                    people.setFoto(icono);
                } catch (Exception ex) {
                    people.setFoto(null);
                }
                int res = sql.actualizar(people);
                if (res > 0) {
                    Toolkit.getDefaultToolkit().beep();
                    limpiartabla();
                    listar();
                    Clear();
                } else {
                    Toolkit.getDefaultToolkit().beep();
                    txtdoc.setEditable(true);
                    txtdoc.setEnabled(true);
                    txtdoc.setText("");

                }
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar este registro", "Si/No", 0);
        if (s == 0) {
            int res = 0;
            int row = jtRegistro.getSelectedRow();
            int fila = jtRegistro.getSelectedColumnCount();
            if (fila < 1) {
                getToolkit().beep();
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR UN REGISTRO DE LA TABLA", "AVISO", JOptionPane.INFORMATION_MESSAGE);

            } else {
                res = sql.eliminarRegistro(jtRegistro.getValueAt(row, 1).toString());
                if (res > 0) {
                    limpiartabla();
                    listar();
                    Clear();
                }
            }
        }

    }//GEN-LAST:event_btneliminarActionPerformed

