    private void jtRegistroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtRegistroMouseClicked
        txtdoc.setEditable(false);
        txtdoc.setEnabled(false);
        int fila = jtRegistro.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Persona no seleccionada");
        } else {

            CBDocumento.setSelectedItem(jtRegistro.getValueAt(fila, 0).toString());
            txtdoc.setText(jtRegistro.getValueAt(fila, 1).toString());
            txtnombre.setText(jtRegistro.getValueAt(fila, 2).toString());
            txtapellido.setText(jtRegistro.getValueAt(fila, 3).toString());
            String genero = jtRegistro.getValueAt(fila, 4).toString();
            //java.sql.Date fecha = (java.sql.Date) jtRegistro.getValueAt(fila, 5);
            String fecha = jtRegistro.getValueAt(fila, 5).toString();
            Date dat;
            try {
                lblFoto.setIcon(sql.Icon(txtdoc.getText()));
            } catch (Exception e) {

            }
            try {
                dat = new SimpleDateFormat("yyyy-MM-dd").parse(fecha);
                date.setDate(dat);
            } catch (ParseException e) {
                getToolkit().beep();
                System.out.println("Eror en fecha de ingreso");
            }
            if (genero.compareTo("Masculino") == 0) {
                this.btnmasculino.setSelected(true);
                this.btnfemenino.setSelected(false);
            } else if (genero.compareToIgnoreCase("Femenino") == 0) {
                this.btnmasculino.setSelected(false);
                this.btnfemenino.setSelected(true);

            }

        }
    }//GEN-LAST:event_jtRegistroMouseClicked

