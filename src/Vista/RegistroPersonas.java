    void listar() {
        String sql = "SELECT * FROM personas";
        try {
            cn = con.Conectarse();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] persona = new Object[6];
            modelo = (DefaultTableModel) jtRegistro.getModel();
            while (rs.next()) {
                persona[0] = rs.getString("tipo_documento");
                persona[1] = rs.getString("documento");
                persona[2] = rs.getString("nombre");
                persona[3] = rs.getString("apellido");
                persona[4] = rs.getString("genero");
                persona[5] = rs.getDate("fecha_nacimiento");

                modelo.addRow(persona);

            }
            jtRegistro.setModel(modelo);
        } catch (Exception e) {
        }
    }

    void limpiartabla() {
        int filas = jtRegistro.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);

        }
    }

