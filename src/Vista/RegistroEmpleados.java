    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Desea registrar este empleado", "Ingreso empleado confirmacion", 0);
        if (s == 0) {
            if (txtSueldo.getText().isEmpty()) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "*CAMPOS OBLIGATORIOS", "INFORMATIVO", JOptionPane.INFORMATION_MESSAGE);
            } else {
                boolean estado = false;
                double sueldo = 0;
                try {
                    sueldo = Double.parseDouble(txtSueldo.getText());
                    estado = true;
                } catch (Exception e) {
                }
                //String buscar = metodosSQLEmpleados.BuscarNombre(txtdoc.getText());
                //String ver = metodosSQLEmpleados.BuscarApellido(txtdoc.getText());

                Empleados emp = new Empleados(txtdoc.getText(), sueldo, (String) CBRol.getSelectedItem(), estado);
                int i = sql.guardar(emp);
                if (i > 0) {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, "EMPLEADO REGISTRADA CON EXITO", "INFORMATIVO", JOptionPane.INFORMATION_MESSAGE);
                    limpiartabla();
                    listar();
                    Clear();
                } else {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, "NO REGISTRADO, YA EXISTE EL EMPLEADO", "ERROR", JOptionPane.ERROR_MESSAGE);

                }

                txtdoc.setEditable(true);
                txtdoc.setEnabled(true);
                txtdoc.setText("");
            }
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar este empleado", "Aviso de Confirmacion", 0);
        if (s == 0) {
            int res = 0;
            int row = jtRegistro1.getSelectedRow();
            int fila = jtRegistro1.getSelectedColumnCount();
            if (fila < 1) {
                getToolkit().beep();
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR UN REGISTRO DE LA TABLA", "AVISO", JOptionPane.INFORMATION_MESSAGE);

            } else {
                res = sql.eliminarRegistro(jtRegistro1.getValueAt(row, 0).toString());
                if (res > 0) {
                    limpiartabla();
                    listar();
                    Clear();
                }
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Desea modificar al empleado", "Si/No", 0);
        if (s == 0) {
            if (txtSueldo.getText().isEmpty()) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "*CAMPOS OBLIGATORIOS", "INFORMATIVO", JOptionPane.INFORMATION_MESSAGE);
            } else {

                double sueldo = 0;
                int id = 0;
                try {
                    sueldo = Double.parseDouble(txtSueldo.getText());
                    id = Integer.parseInt(txtid.getText());
                } catch (Exception e) {
                }

                Empleados emp = new Empleados(id, sueldo, (String) CBRol.getSelectedItem());
                int i = sql.actualizar(emp);

                if (i > 0) {
                    Toolkit.getDefaultToolkit().beep();

                    limpiartabla();
                    listar();
                    Clear();
                } else {
                    Toolkit.getDefaultToolkit().beep();

                }
                txtdoc.setEditable(true);
                txtdoc.setEnabled(true);
                txtdoc.setText("");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

