    void listar() {
        String sql = "SELECT idempleado,empleados.documento,nombre,apellido,sueldo,trabajo FROM empleados,personas WHERE empleados.documento=personas.documento";
        try {
            cn = con.Conectarse();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] persona = new Object[6];
            modelo = (DefaultTableModel) jtRegistro1.getModel();
            while (rs.next()) {
                persona[0] = rs.getInt("idempleado");
                persona[1] = rs.getString("documento");
                persona[2] = rs.getString("nombre");
                persona[3] = rs.getString("apellido");
                persona[4] = rs.getDouble("sueldo");
                persona[5] = rs.getString("trabajo");

                modelo.addRow(persona);
            }
            jtRegistro1.setModel(modelo);
        } catch (Exception e) {
        }
    }

    void limpiartabla() {
        int filas = jtRegistro1.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);

        }
    }

