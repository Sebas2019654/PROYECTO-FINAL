    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int s = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar este registro", "Si/No", 0);
        if (s == 0) {
            int res = 0;
            int row = jUsu.getSelectedRow();
            int fila = jUsu.getSelectedColumnCount();
            if (fila < 1) {
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR UN REGISTRO DE LA TABLA", "AVISO", JOptionPane.INFORMATION_MESSAGE);

            } else {
                res = sql.eliminarRegistro(jUsu.getValueAt(row, 2).toString());
                if (res >= 0) {
                    limpiartabla();
                    listar();
                    clear();
                }
            }
        }

    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        int s = JOptionPane.showConfirmDialog(null, "Esta seguro de modificar este usuario", "Si/No", 0);
        if (s == 0) {
            if (txtNombre.getText().isEmpty() || txtapellido.getText().isEmpty() || txtusuario.getText().isEmpty() || txtPasword.getText().isEmpty() || txtrecuperar.getText().isEmpty()) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "*CAMPOS OBLIGATORIOS");
            } else {
                boolean estado;
                int rol;
                if (cboxrango.getSelectedItem().equals("ADMINISTRADOR")) {
                    rol = 1;
                    estado = true;
                } else {
                    rol = 2;
                    estado = true;
                }
                Usuario u = new Usuario(txtNombre.getText(), txtapellido.getText(), txtusuario.getText(), txtPasword.getText(), (String) cboxrango.getSelectedItem(), estado, rol, txtrecuperar.getText());
                int i = sql.actualizar(u);

                limpiartabla();
                listar();
                txtNombre.setText("");
                txtapellido.setText("");
                txtusuario.setText("");
                txtPasword.setText("");
                cboxrango.setSelectedItem("OPCIONES");
                txtrecuperar.setText("");

            }
        }

    }//GEN-LAST:event_btnguardarActionPerformed

