    void listar() {
        String sql = "SELECT * FROM usuarios";
        try {
            cn = con.Conectarse();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] usuario = new Object[6];
            modelo = (DefaultTableModel) jUsu.getModel();
            while (rs.next()) {
                usuario[0] = rs.getString("nombre");
                usuario[1] = rs.getString("apellido");
                usuario[2] = rs.getString("usuario");
                usuario[3] = rs.getString("password");
                usuario[4] = rs.getString("rango");
                usuario[5] = rs.getString("recuperarcodigo");

                modelo.addRow(usuario);

            }
            jUsu.setModel(modelo);
        } catch (Exception e) {
        }
    }

    void limpiartabla() {
        int filas = jUsu.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);

        }
    }

